using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace YDS6000.Models
{
    /// <summary>
    /// 阿里短信模板
    /// </summary>
    public class AliSmsConfig
    {
        /// <summary>
        /// HTTP请求地址
        /// </summary>
        public string AliUrl { get; set; }
        /// <summary>
        /// TOP分配给应用的AppKey。
        /// </summary>
        public string AliAppKey { get; set; }
        /// <summary>
        /// App Secret
        /// </summary>
        public string AliAppSecret { get; set; }
        /// <summary>
        /// App 签名名称
        /// </summary>
        public string AliSignName { get; set; }
        /// <summary>
        /// App 模板ID
        /// </summary>
        public string AliTemplateCode { get; set; }
    }

    /// <summary>
    /// 短信COM端口
    /// </summary>
    public class ComSmsConfig
    {
        /// <summary>
        /// com口
        /// </summary>
        public string ComSms { get; set; }
    }

    /// <summary>
    /// 微信对象
    /// </summary>
    public class WxConfig
    {
        /// <summary>
        /// 绑定支付的APPID（必须配置）
        /// </summary>
        public string WxAPPID { get; set; }
        /// <summary>
        /// 商户号（必须配置）
        /// </summary>
        public string WxMCHID { get; set; }
        /// <summary>
        /// 商户支付密钥，参考开户邮件设置（必须配置）
        /// </summary>
        public string WxKEY { get; set; }
        /// <summary>
        /// 公众帐号secert（仅JSAPI支付的时候需要配置）
        /// </summary>
        public string WxAPPSECRET { get; set; }
        /// <summary>
        /// 公众帐号回调页面（仅JSAPI支付的时候需要配置）
        /// </summary>
        public string WxRedirect_uri { get; set; }
    }
    /// <summary>
    /// email 对象
    /// </summary>
    public class EmailConfig
    {
        /// <summary>
        /// 邮件发送人
        /// </summary>
        public string MailFrom { get; set; }
        /// <summary>
        /// 邮件服务器地址
        /// </summary>
        public string MailSmtpHost { get; set; }
        /// <summary>
        /// 邮件登陆名
        /// </summary>
        public string MailSmtpUser { get; set; }
        /// <summary>
        /// 邮件登陆密码
        /// </summary>
        public string MailSmtpPassword { get; set; }
    }
}
